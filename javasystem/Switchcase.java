package javasystem;

public class Switchcase {

	public static void main(String[] args) {
	 int x=50;
	 switch (x) {
	 case 50: System.out.println(5); break;
	 case 20: System.out.println(10); break;
	 case 30: System.out.println(20); break;
	 
	 default:System.out.println("Not in 5, 10 or 20");
	 }

	}

}
