package practice;

public class switchcase {

	public static void main(String[] args) {
		int a=30;
		switch(a) {
		case 40: System.out.println(1); break;
		case 30: System.out.println(3); break;
		case 39: System.out.println(5); break;
		
		default : System.out.println("not is 1,3 or 5");
		}
	}
}
